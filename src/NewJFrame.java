    public void openFile() throws IOException {
        
        JFileChooser openFile = new JFileChooser();
        openFile.showOpenDialog(null);
        BufferedReader bf = null;
        File f = null;
        f = openFile.getSelectedFile();
        if(f==null)
           ;
        else{
          s1=f.getPath();
        if(s1.endsWith(".pdf")||s1.endsWith(".exe"))
        {
            JOptionPane.showMessageDialog(null,"The file type is incompatible with mypad");
            return ;
        }   
            if(f.getName()!=null)
        this.setTitle(f.getName());
        
        
       
            bf = new BufferedReader(new FileReader(f.getPath()));
        
        
        String s,t ="";
        
        while((s=bf.readLine())!=null)
        {
            t+=s;
            t+='\n';
        }
        txt1.setText(t);
        
        if(bf!=null)
        {
            bf.close();
        }
        }
    }
    
